    void setup(ExporterVNA exporter) {
        //get
        //   exportColorsCheckBox.setSelected(exporter.isExportColor());
        exportColorsCheckBox.setSelected(exporter.isExportColor());
        exportEdgeWeightCheckBox.setSelected(exporter.isExportEdgeWeight());
        exportLabelCheckBox.setSelected(exporter.isExportShortLabel());
        exportAttributesCheckBox.setSelected(exporter.isExportAttributes());
        exportCoordsCheckBox.setSelected(exporter.isExportCoords());
        exportSizeCheckBox.setSelected(exporter.isExportSize());
        normalizeCheckBox.setSelected(exporter.isNormalize());
    }

    void unsetup(ExporterVNA exporter) {
        exporter.setExportAttributes(exportAttributesCheckBox.isSelected());
        exporter.setExportColor(exportColorsCheckBox.isSelected());
        exporter.setExportCoords(exportCoordsCheckBox.isSelected());
        exporter.setExportEdgeWeight(exportEdgeWeightCheckBox.isSelected());
        exporter.setExportShortLabel(exportLabelCheckBox.isSelected());
        exporter.setExportSize(exportSizeCheckBox.isSelected());
        exporter.setNormalize(normalizeCheckBox.isSelected());
    }

